<?php
// inc/auth.php
require_once __DIR__ . '/config.php';

function require_login(){
    if(empty($_SESSION['user_id'])){
        header('Location: /login.php'); exit;
    }
}

function require_admin_login(){
    if(empty($_SESSION['admin_id'])){
        header('Location: /admin/login.php'); exit;
    }
}
?>
